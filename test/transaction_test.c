#include <sodium.h>

#include "deps/greatest.h"
#include "../src/transaction.h"

SUITE(transaction_suite);

TEST can_sign_txin(void) {
  struct InputTransaction txin = {
    .transaction = {
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00
    },
    .txout_index = 0
  };

  struct OutputTransaction txout = {
    .amount = 50,
    .address = {
      0x01, 0x3e, 0x46, 0xa5,
      0xc6, 0x99, 0x4e, 0x35,
      0x55, 0x50, 0x1c, 0xba,
      0xc0, 0x7c, 0x06, 0x77
    }
  };

  struct OutputTransaction *txout_p = &txout;
  struct Transaction tx = {
    .txin_count = 1,
    .txout_count = 1,
    .txouts = &txout_p
  };

  unsigned char pk[crypto_sign_PUBLICKEYBYTES];
  unsigned char sk[crypto_sign_SECRETKEYBYTES];

  crypto_sign_keypair(pk, sk);
  sign_txin(&txin, &tx, pk, sk);

  ASSERT_MEM_EQ(pk, txin.public_key, crypto_sign_PUBLICKEYBYTES);

  PASS();
}

GREATEST_SUITE(transaction_suite) {
  RUN_TEST(can_sign_txin);
}
